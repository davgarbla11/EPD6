SELECT COUNTRY_NAME, REGION_NAME
FROM HR.COUNTRIES A, HR.REGIONS B
WHERE B.REGION_ID = A.REGION_ID AND B.REGION_ID < 3;

SELECT COUNTRY_NAME, REGION_NAME
FROM HR.REGIONS A, HR.COUNTRIES B
WHERE A.REGION_ID = B.REGION_ID AND B.COUNTRY_NAME LIKE'E%';

SELECT COUNTRY_NAME, REGION_NAME
FROM HR.REGIONS A NATURAL JOIN HR.COUNTRIES B
WHERE B.COUNTRY_NAME LIKE'E%';

SELECT EMPLOYEE_ID, JOB_ID, DEPARTMENT_ID, LAST_NAME, HIRE_DATE, END_DATE
FROM HR.EMPLOYEES A NATURAL JOIN HR.JOB_HISTORY B;

SELECT A.EMPLOYEE_ID, A.JOB_ID, A.DEPARTMENT_ID, LAST_NAME, HIRE_DATE, END_DATE
FROM HR.EMPLOYEES A, HR.JOB_HISTORY B
WHERE A.EMPLOYEE_ID = B.EMPLOYEE_ID;

SELECT EMPLOYEE_ID, JOB_ID, A.DEPARTMENT_ID, LAST_NAME, HIRE_DATE, END_DATE
FROM HR.EMPLOYEES A JOIN HR.JOB_HISTORY B USING(JOB_ID, EMPLOYEE_ID);

