SELECT * FROM HR.DEPARTMENTS;

SELECT A.FIRST_NAME, B.DEPARTMENT_NAME 
FROM HR.EMPLOYEES A, HR.DEPARTMENTS B
WHERE A.DEPARTMENT_ID = B.DEPARTMENT_ID
ORDER BY 1;

SELECT A.FIRST_NAME, B.DEPARTMENT_NAME
FROM HR.EMPLOYEES A
LEFT OUTER JOIN HR.DEPARTMENTS B
ON(A.DEPARTMENT_ID = B.DEPARTMENT_ID)
ORDER BY 1;

SELECT A.FIRST_NAME, B.DEPARTMENT_NAME
FROM HR.EMPLOYEES A
RIGHT OUTER JOIN HR.DEPARTMENTS B
ON(A.DEPARTMENT_ID = B.DEPARTMENT_ID)
ORDER BY 1;

SELECT A.FIRST_NAME, B.DEPARTMENT_NAME
FROM HR.EMPLOYEES A
FULL OUTER JOIN HR.DEPARTMENTS B
ON(A.DEPARTMENT_ID = B.DEPARTMENT_ID)
ORDER BY 1;

SELECT A.LAST_NAME, A.SALARY, B.DEPARTMENT_NAME, C.CITY, D.COUNTRY_NAME, E.REGION_NAME
FROM HR.EMPLOYEES A, HR.DEPARTMENTS B, HR.LOCATIONS C, HR.COUNTRIES D, HR.REGIONS E
WHERE A.DEPARTMENT_ID = B.DEPARTMENT_ID AND B.LOCATION_ID = C.LOCATION_ID AND C.COUNTRY_ID = D.COUNTRY_ID AND D.REGION_ID = E.REGION_ID AND A.SALARY > 12000;

SELECT A.LAST_NAME, A.SALARY, A.JOB_ID, B.JOB_ID, B.MAX_SALARY
FROM HR.EMPLOYEES A, HR.JOBS B
WHERE B.MAX_SALARY >= 2*A.SALARY AND A.SALARY > 5000
ORDER BY 1;







